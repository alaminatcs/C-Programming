#include<stdio.h>
int main()
{
    printf("%d\n", 5>2);
    printf("%d\n", 5<2);
    printf("%d\n", 5==2);
    printf("%d\n", 5!=2);
    return 0;
}
/*
    relational operators state the relation
    between two statement whether it is true or not?
    here 1-indicates True
    and  0-indicates False
*/