#include<stdio.h>
int main()
{
    int a=5, b=2;
    printf("summation: %d\n", a+b);
    printf("subtraction: %d\n", a-b);
    printf("multiplication: %d\n", a*b);
    printf("division: %d\n", a/b);
    printf("floating division: %f\n", a*1.0/b);
    printf("remainder: %d\n", a%b);
    return 0;
}
/*
    arithmetic operators do the arithmetic operations.
    -division between two integer always gives an integer.
*/